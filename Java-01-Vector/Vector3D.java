public class Vector3D
{
      private double x = 0;
      private double y = 0;
      private double z = 0;
      
    public Vector3D(double x, double y, double z)
    {
       this.x = x;
       this.y = y;
       this.z = z;
    }
    
    public double getX(){
        return x;
    }
    
    public double getY(){
        return y;
    }
    
    public double getZ(){
        return z;
    }
    
}
